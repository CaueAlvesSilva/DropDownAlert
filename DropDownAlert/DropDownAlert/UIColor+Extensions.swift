import Foundation
import UIKit

extension UIColor {
    class func successDropDownAlertColor() -> UIColor {
        return UIColor(red: 40/255, green: 72/255, blue: 50/255, alpha: 1.0)
    }
    
    class func failDropDownAlertColor() -> UIColor {
        return UIColor(red: 153/255, green: 17/255, blue: 17/255, alpha: 1.0)
    }
}
